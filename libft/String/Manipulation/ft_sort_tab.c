#include "../../libft.h"

char	**sort_tab(char **tab)
{
	int	i;

	i = 0;
	while (tab[i] && tab[i + 1])
	{
		if (ft_strcmp(tab[i], tab[i + 1]) >= 0)
		{
			swap_string(&tab[i], &tab[i + 1]);
			i = 0;
		}
		i++;
	}
	return (tab);
}
